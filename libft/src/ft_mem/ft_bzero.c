#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp_pt;

	tmp_pt = (unsigned char *) s;
	while (n > 0)
	{
		*(tmp_pt++) = '\0';
		n--;
	}
}
